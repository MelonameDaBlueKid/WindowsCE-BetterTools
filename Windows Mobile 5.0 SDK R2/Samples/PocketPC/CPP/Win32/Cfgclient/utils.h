// Check HRESULT
#define CHR(hResult)\
    do {hr = (hResult); if(FAILED(hr)) goto Error;} while (0,0)

// Check boolean result
#define CBR(e)\
    do {if (!(e)) {hr = E_FAIL; goto Error;} } while (0,0)


